package az.coders.java_lessons.lesson_8.overloading;

public class Math {

    public static int sum() {
        return 0;
    }

    public static int sum(int a, int b) {
        return a + b;
    }

    public static int sum(int a, int b, int c) {
        return a + b + c;
    }

    public static int sum(int a, int b, int c, int d) {
        return a + b + c + d;
    }
}
